package accbook.vo;

public class AccExpList
{
    private int expSeq;
    private String artiKinds, userRea, regDate, updateDate, artiMoney;

    public int getExpSeq()
    {
        return expSeq;
    }

    public void setExpSeq(int expSeq)
    {
        this.expSeq = expSeq;
    }

    public String getArtiKinds()
    {
        return artiKinds;
    }

    public void setArtiKinds(String artiKinds)
    {
        this.artiKinds = artiKinds;
    }

    public String getUserRea()
    {
        return userRea;
    }

    public void setUserRea(String userRea)
    {
        this.userRea = userRea;
    }

    public String getRegDate()
    {
        return regDate;
    }

    public void setRegDate(String regDate)
    {
        this.regDate = regDate;
    }

    public String getUpdateDate()
    {
        return updateDate;
    }

    public void setUpdateDate(String updateDate)
    {
        this.updateDate = updateDate;
    }

    public String getArtiMoney()
    {
        return artiMoney;
    }

    public void setArtiMoney(String artiMoney)
    {
        this.artiMoney = artiMoney;
    }
}
