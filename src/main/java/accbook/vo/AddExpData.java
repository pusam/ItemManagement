package accbook.vo;

public class AddExpData
{
    private String date, useRea, loginID, money;
    private int kinds;

    public String getDate()
    {
        return date;
    }

    public void setDate(String date)
    {
        this.date = date;
    }

    public String getUseRea()
    {
        return useRea;
    }

    public void setUseRea(String useRea)
    {
        this.useRea = useRea;
    }

    public String getLoginID()
    {
        return loginID;
    }

    public void setLoginID(String loginID)
    {
        this.loginID = loginID;
    }

    public String getMoney()
    {
        return money;
    }

    public void setMoney(String money)
    {
        this.money = money;
    }

    public int getKinds()
    {
        return kinds;
    }

    public void setKinds(int kinds)
    {
        this.kinds = kinds;
    }
}
