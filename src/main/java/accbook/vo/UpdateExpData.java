package accbook.vo;

public class UpdateExpData
{
    private int expSeq;
    private String date, kinds, useRea, money;

    public int getExpSeq()
    {
        return expSeq;
    }

    public void setExpSeq(int expSeq)
    {
        this.expSeq = expSeq;
    }

    public String getDate()
    {
        return date;
    }

    public void setDate(String date)
    {
        this.date = date;
    }

    public String getKinds()
    {
        return kinds;
    }

    public void setKinds(String kinds)
    {
        this.kinds = kinds;
    }

    public String getUseRea()
    {
        return useRea;
    }

    public void setUseRea(String useRea)
    {
        this.useRea = useRea;
    }

    public String getMoney()
    {
        return money;
    }

    public void setMoney(String money)
    {
        this.money = money;
    }
}
